    public void loadAllDataIntoTable(List<personne> list){
      
        DefaultTableModel dm= (DefaultTableModel) jTable1.getModel();
        dm.setRowCount(0);
        
         for(personne p:list){
            Vector v = new Vector();
            v.add(p.getId_employee());
            v.add(p.getNom());
            v.add(p.getPrenom());
            v.add(p.getAdresse());
            v.add(p.getNumero_tel());
            v.add(p.getGenre());

            dm.addRow(v);
       } 
}
    
