    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        if(row < 0 ){
        JOptionPane.showMessageDialog(null, "please select any row from the table");
        
        }else{
        int id = (int) jTable1.getValueAt(row, 0);
            sqlFunctions fn = new sqlFunctions();
            personne perso = fn.returnAllDataToTextField(id);
            nomfield.setText(perso.getNom());
            prenomfield.setText(perso.getPrenom());
            adressefield.setText(perso.getAdresse());
            numerofield.setText(perso.getNumero_tel());
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

